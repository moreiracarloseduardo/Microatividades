# Microatividade 4: Estrutura de repetição for em Python

# variável texto
texto = 'Olá, laço for.'

# Iterando sobre cada caractere do texto
for item in texto:
    print('Caractere: ' + item)

print('\n')  

# Iterando sobre um intervalo numérico de 1 a 10
for numero in range(1, 11):
    print('Número do intervalo: ' + str(numero))
